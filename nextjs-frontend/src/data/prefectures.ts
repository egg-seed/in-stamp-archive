export function getPrefectureByCode(code: string): PrefectureMetadata | undefined {
  return prefectures.find((prefecture) => prefecture.code === code);
}

export function getPrefecturesByRegion(): Record<PrefectureRegion, PrefectureMetadata[]> {
  return PREFECTURE_REGIONS.reduce<Record<PrefectureRegion, PrefectureMetadata[]>>(
    (acc, region) => {
      acc[region] = prefectures
        .filter((prefecture) => prefecture.region === region)
        .sort((a, b) => a.kana.localeCompare(b.kana, "ja"));
      return acc;
    },
    {} as Record<PrefectureRegion, PrefectureMetadata[]>,
  );
}
